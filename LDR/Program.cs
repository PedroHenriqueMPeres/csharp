Console.WriteLine("Com While");
int numero = 1;
while (numero <= 10)
{
    Console.WriteLine(numero);
    numero++;
}

Console.WriteLine("Com Do While");

numero = 1;
do
{
    Console.WriteLine(numero);
    numero++;
    
} while (numero <= 10);

Console.WriteLine("Com For");

for (int n = 1; n <= 10; n++)
{
    Console.WriteLine(n);
}